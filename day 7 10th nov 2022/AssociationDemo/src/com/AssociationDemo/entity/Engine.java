package com.AssociationDemo.entity;

public class Engine {
	private int capacity;
	private String fuel;
	
	public Engine() {
		this.capacity=capacity;
		this.fuel=fuel;
	}

	public Engine() {
		
	}

	public int getCapacity() {
		return capacity;
	}

	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}

	public String getFuel() {
		return fuel;
	}

	public void setFuel(String fuel) {
		this.fuel = fuel;
	}
}
